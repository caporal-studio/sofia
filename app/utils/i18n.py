def get_language() -> str:
    language = st.session_state.get(
        "language_selector",
        st.session_state.get("language", DEFAULT_LANGUAGE),
    )
    if language not in SUPPORTED_LANGUAGES:
        return DEFAULT_LANGUAGE
    return language


def t(key: str, **kwargs) -> str:
    text = TRANSLATIONS.get(get_language(), TRANSLATIONS[DEFAULT_LANGUAGE]).get(
        key,
        TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key),
    )
    if kwargs:
        return text.format(**kwargs)
    return text


def language_selector(container=None):
    target = container if container is not None else st.sidebar
    current = get_language()
    options = list(SUPPORTED_LANGUAGES.keys())
    target.markdown(
        f"<div style='padding-left: 2rem; font-weight: 600; margin-bottom: 0.25rem;'>{t('language')}</div>",
        unsafe_allow_html=True,
    )
    selected = target.selectbox(
        "language",
        options=options,
        index=options.index(current),
        format_func=lambda code: SUPPORTED_LANGUAGES[code],
        key="language_selector",
        label_visibility="collapsed",
    )
    st.session_state["language"] = selected
    return selected


def localized_subtitle(info: dict) -> str:
    language = get_language()
    localized_value = info.get(f"subtitle_{language}")
    if localized_value:
        return localized_value

    subtitle = info.get("subtitle", t("app_subtitle"))
    normalized_subtitle = subtitle.replace("ç", "c").replace("ã", "a").replace("á", "a").replace("é", "e")
    if language == "en" and normalized_subtitle.startswith("Sistema de Organizacao"):
        return t("app_subtitle")
    return subtitle
